

public class Deck {
    public static void main(String[] args)
    {
        // initializing array
        int[] arr = {40,10,25,63};

        // size of array
        int n = arr.length;

        // traversing array
        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");
    }
}
